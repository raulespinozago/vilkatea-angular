-- Crea usuario y tabla CLIENTE para la prueba
CREATE USER app_clientes IDENTIFIED BY App_Cl1entes#2025
  DEFAULT TABLESPACE USERS
  QUOTA UNLIMITED ON USERS;

GRANT CONNECT, RESOURCE TO app_clientes;

ALTER SESSION SET CURRENT_SCHEMA = app_clientes;

CREATE TABLE CLIENTE (
  ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RUC             VARCHAR2(11) NOT NULL,
  RAZON_SOCIAL    VARCHAR2(200) NOT NULL,
  TELEFONO        VARCHAR2(20),
  CORREO          VARCHAR2(200),
  DIRECCION       VARCHAR2(300)
);

CREATE UNIQUE INDEX UX_CLIENTE_RUC ON CLIENTE(RUC);

